#!/usr/bin/env node
/**
 * Enlight ITSM — one-command deploy tool.
 *
 *   node deploy.mjs            interactive deploy wizard
 *   node deploy.mjs destroy    tear down the last deployment
 *   node deploy.mjs --generate-only   write config files but don't run Terraform
 *
 * Provisions a single VM (GCP / AWS / DigitalOcean) that runs the full Enlight
 * stack via Docker Compose: the app (built from source), PostgreSQL+pgvector,
 * and Redis. No prior infrastructure knowledge required.
 */

import readline from 'node:readline';
import crypto from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawn, spawnSync } from 'node:child_process';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const STATE_FILE = path.join(__dirname, '.enlight-deploy.json');

// ── Pretty output ────────────────────────────────────────────────────────────
const c = {
  reset: '\x1b[0m', bold: '\x1b[1m', dim: '\x1b[2m',
  cyan: '\x1b[36m', green: '\x1b[32m', yellow: '\x1b[33m', red: '\x1b[31m', blue: '\x1b[34m',
};
const color = (col, s) => `${c[col]}${s}${c.reset}`;
const log = (s = '') => console.log(s);
const ok = (s) => log(color('green', `✓ ${s}`));
const warn = (s) => log(color('yellow', `! ${s}`));
const err = (s) => log(color('red', `✗ ${s}`));

function banner() {
  log();
  log(color('cyan', '  ╔═══════════════════════════════════════════╗'));
  log(color('cyan', '  ║') + color('bold', '        Enlight ITSM — Deploy Tool         ') + color('cyan', '║'));
  log(color('cyan', '  ╚═══════════════════════════════════════════╝'));
  log(color('dim', '  Stand up a production instance in a few minutes.'));
  log();
}

// ── Prompt helpers ───────────────────────────────────────────────────────────
// A buffered line reader — works reliably for both interactive (TTY) and piped
// input (rl.question only reads the first piped line on recent Node versions).
function makeReader() {
  const rl = readline.createInterface({ input: process.stdin });
  const buf = [];
  const waiters = [];
  let closed = false;
  rl.on('line', (l) => { if (waiters.length) waiters.shift()(l); else buf.push(l); });
  rl.on('close', () => { closed = true; while (waiters.length) waiters.shift()(null); });
  return {
    next() {
      if (buf.length) return Promise.resolve(buf.shift());
      if (closed) return Promise.resolve(null);
      return new Promise((res) => waiters.push(res));
    },
    close() { rl.close(); },
  };
}
const reader = makeReader();
const rl = { close: () => reader.close() };
async function question(prompt) {
  process.stdout.write(prompt);
  const line = await reader.next();
  return line === null ? '' : line;
}

async function ask(label, { def = '', required = false, secret = false } = {}) {
  const hint = def ? color('dim', ` [${secret ? '••••' : def}]`) : (required ? color('dim', ' (required)') : color('dim', ' (optional)'));
  for (;;) {
    const a = (await question(`  ${label}${hint}: `)).trim();
    const val = a || def;
    if (required && !val) { err('  This value is required.'); continue; }
    return val;
  }
}

async function select(label, options) {
  log(`  ${color('bold', label)}`);
  options.forEach((o, i) => {
    log(`    ${color('cyan', String(i + 1))}) ${o.label}`);
    if (o.desc) log(`       ${color('dim', o.desc)}`);
  });
  for (;;) {
    const a = (await question(`  Choose ${color('dim', '[1-' + options.length + ']')}: `)).trim();
    const n = parseInt(a, 10);
    if (n >= 1 && n <= options.length) return options[n - 1].value;
    err('  Enter a number from the list.');
  }
}

async function confirm(label, def = true) {
  const a = (await question(`  ${label} ${color('dim', def ? '[Y/n]' : '[y/N]')}: `)).trim().toLowerCase();
  if (!a) return def;
  return a === 'y' || a === 'yes';
}

// Print dimmed, indented guidance (a string or an array of lines).
function info(text) {
  for (const line of (Array.isArray(text) ? text : [text])) log(color('dim', `  ${line}`));
}

// A numbered step header.
function section(n, total, title) {
  log();
  log(color('cyan', `  ─── Step ${n}/${total} · ${color('bold', title)}${c.reset}${c.cyan} ───`));
  log();
}

// A "Requirements:" block listing what a step needs.
function reqs(items) {
  log(color('yellow', '  What you need for this step:'));
  for (const it of items) log(`    ${color('yellow', '•')} ${color('dim', it)}`);
  log();
}

// ── Secret generation ────────────────────────────────────────────────────────
const randHex = (bytes) => crypto.randomBytes(bytes).toString('hex');
const randPass = () => crypto.randomBytes(18).toString('base64url'); // url-safe, no special chars

// ── .env builder ─────────────────────────────────────────────────────────────
