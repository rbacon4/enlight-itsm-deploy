// ── Deploy flow ──────────────────────────────────────────────────────────────
async function deploy(generateOnly) {
  banner();
  info([
    'This wizard provisions one small VM on your cloud and runs the full Enlight',
    'stack on it (app + database + queue + HTTPS proxy). It takes ~5–10 minutes.',
    'Everything you enter is saved locally; nothing is sent anywhere but your cloud.',
    'You can stop and re-run this command at any time to pick up where you left off.',
  ]);

  // ── Resume detection ──
  // If a previous run left config (or a partial/complete provision) behind, offer
  // to continue it instead of starting from scratch.
  const prev = generateOnly ? null : existingDeployment();
  if (prev) {
    const pdef = CLOUDS[prev.cloud];
    log();
    if (prev.provisioned) {
      ok(`Found an existing ${pdef?.label ?? prev.cloud} deployment.`);
      info(['Re-applying is safe and idempotent — use it to finish an interrupted run or push config changes.']);
    } else {
      ok(`Found a saved ${pdef?.label ?? prev.cloud} configuration that hasn\'t been provisioned yet.`);
      info(['You can finish provisioning it now without re-entering anything.']);
    }
    const choice = await select('What would you like to do?', [
      { label: prev.provisioned ? 'Resume / update this deployment' : 'Continue provisioning it', value: 'resume',
        desc: 'Re-run Terraform with the saved settings (recommended).' },
      { label: 'Start a new deployment', value: 'new',
        desc: 'Re-enter everything. Overwrites the saved config for that cloud.' },
      { label: 'Tear it down', value: 'destroy', desc: 'Destroy the cloud resources and remove state.' },
    ]);
    if (choice === 'resume') {
      await provision(pdef ?? { dir: prev.dir, label: prev.cloud }, readDomainFromEnv(prev.dir));
      return;
    }
    if (choice === 'destroy') {
      await destroy();
      return;
    }
    // 'new' → fall through to the fresh wizard below.
    log();
    warn('Starting a new deployment. The previous config for the chosen cloud will be overwritten.');
  }

  // ── Step 1: cloud ──
  section(1, 4, 'Choose your cloud');
  reqs([
    'An account with one of: Google Cloud, AWS, or DigitalOcean.',
    'Terraform — installed automatically later if you don\'t have it.',
  ]);
  info(['Enlight runs on a single VM. Pick where to create it.']);
  const cloud = await select('Cloud provider', [
    { label: CLOUDS.gcp.label, value: 'gcp', desc: CLOUDS.gcp.menuDesc },
    { label: CLOUDS.aws.label, value: 'aws', desc: CLOUDS.aws.menuDesc },
    { label: CLOUDS.digitalocean.label, value: 'digitalocean', desc: CLOUDS.digitalocean.menuDesc },
  ]);
  const def = CLOUDS[cloud];
  log();
  info(def.credsLines);

  log();
  info([
    'Source access: the server builds Enlight from its GitHub repo. If that repo is',
    'PRIVATE, paste a GitHub token (classic, with "repo" scope, or fine-grained with',
    'read access) so the server can clone it. Public repo? Just press Enter.',
    'Create one at https://github.com/settings/tokens',
  ]);
  const githubToken = await ask('GitHub access token (only if the app repo is private)', { secret: true });

  // ── Step 2: AI ──
  section(2, 4, 'AI configuration');
  reqs([
    'Optional: an API key for your AI platform (Anthropic or OpenAI) — or add it in-app later.',
    'Optional: an embeddings key (Voyage or OpenAI) for knowledge base search.',
  ]);
  info([
    'Enlight\'s agent uses a large language model to triage and answer tickets.',
    'Choose a platform now — you can change it any time in Settings → AI Keys.',
  ]);
  const aiProvider = await select('AI platform', [
    { label: 'Anthropic (Claude)', value: 'anthropic', desc: 'Recommended — the platform Enlight is built and tuned for.' },
    { label: 'OpenAI (GPT)', value: 'openai', desc: 'Use GPT-4o / GPT-4.1 models instead.' },
  ]);
  let anthropicKey = '', openaiKey = '';
  log();
  if (aiProvider === 'anthropic') {
    info(['Get a key at https://console.anthropic.com → API Keys.',
          'You can leave this blank and add it later in the app (the agent stays idle until then).']);
    anthropicKey = await ask('Anthropic API key', { secret: true });
  } else {
    info(['Get a key at https://platform.openai.com/api-keys.',
          'You can leave this blank and add it later in the app (the agent stays idle until then).']);
    openaiKey = await ask('OpenAI API key', { secret: true });
  }

  log();
  info(['Knowledge base search uses an embeddings provider (separate from the chat model above).']);
  const embeddingProvider = await select('Embeddings provider', [
    { label: 'Voyage AI', value: 'voyage', desc: 'Recommended — voyage-large-2, free tier at voyageai.com.' },
    { label: 'OpenAI', value: 'openai', desc: 'text-embedding-3-small — reuses your OpenAI key.' },
  ]);
  let voyageKey = '';
  log();
  if (embeddingProvider === 'voyage') {
    info(['Get a key at https://www.voyageai.com (free tier).',
          'Leave blank to skip knowledge base search for now — you can add it later.']);
    voyageKey = await ask('Voyage API key', { secret: true });
  } else if (!openaiKey) {
    info(['OpenAI embeddings need an OpenAI key (you didn\'t enter one above).']);
    openaiKey = await ask('OpenAI API key', { secret: true });
  }

  // ── Step 3: domain ──
  section(3, 4, 'Domain & HTTPS');
  reqs([
    'Optional: a domain name (or subdomain) you own.',
    'Optional: access to that domain\'s DNS to add an A record after provisioning.',
    'No domain? You can skip this and still deploy (plain HTTP).',
  ]);
  info([
    'If you have a domain, Enlight gets automatic HTTPS (Caddy + Let\'s Encrypt) — no certs to buy.',
    'After this finishes you\'ll point a DNS A record at the server\'s IP (shown at the end).',
    'A subdomain like  itsm.yourcompany.com  is recommended.',
    'Leave blank to serve plain HTTP on port 80 (you can add a domain later).',
  ]);
  const domain = await ask('Domain for this server', {});

  // ── Step 4: cloud settings ──
  section(4, 4, `${def.label} settings`);
  reqs([
    `Your ${def.label} credentials ready (the checklist shown after Step 1).`,
    'Region / size values — sensible defaults are provided, just press Enter to accept.',
  ]);
  const cloudVars = await def.prompts();
  log();

  // ── Generate secrets + write files ──
  const app = {
    aiProvider, embeddingProvider, anthropicKey, openaiKey, voyageKey, domain, githubToken,
    pgPassword: randPass(),
    jwtSecret: randHex(24),
    sessionSecret: randHex(24),
    encKey: randHex(32), // 64 hex chars = 32 bytes
  };
  const tfDir = path.join(__dirname, def.dir);
  fs.writeFileSync(path.join(tfDir, '.env'), buildEnv(app));
  fs.writeFileSync(path.join(tfDir, 'terraform.tfvars'), buildTfvars(cloudVars));
  ok(`Wrote ${def.dir}/.env and terraform.tfvars`);
  log(color('dim', '  Secrets were auto-generated. These files contain credentials — keep them private.'));
  log();

  saveState({ cloud, dir: def.dir });

  if (generateOnly) {
    ok('Generated config only (--generate-only). Run terraform yourself when ready:');
    info([`  terraform -chdir=${def.dir} init && terraform -chdir=${def.dir} apply`]);
    rl.close();
    return;
  }

  await provision(def, domain);
}

// ── Provision (shared by fresh deploys and resumes) ──────────────────────────
async function provision(def, domain) {
  // Make sure Terraform is available — offer to install it if not.
  let tf = resolveTerraform();
  if (tf) {
    TERRAFORM = tf;
  } else {
    warn('Terraform is required to provision, and it isn\'t installed.');
    const installNow = await confirm('Download and install Terraform now (official HashiCorp binary)?', true);
    if (installNow) {
      try {
        const v = await installTerraform();
        ok(`Terraform ${v} installed into ./bin.`);
        tf = TERRAFORM;
      } catch (e) {
        err(`Automatic install failed: ${e.message}`);
      }
    }
  }
  if (!tf) {
    warn('Config files are ready, but provisioning needs Terraform.');
    info([
      'Install it from https://developer.hashicorp.com/terraform/install, then run:',
      `  terraform -chdir=${def.dir} init && terraform -chdir=${def.dir} apply`,
    ]);
    rl.close();
    return;
  }

  const go = await confirm(`Provision now on ${def.label}? This creates/updates real cloud resources.`, true);
  if (!go) { warn('Skipped provisioning. Config files are saved — run again to resume.'); rl.close(); return; }
  rl.close();

  log();
  log(color('blue', '  → terraform init'));
  if (await runTf(def.dir, ['init', '-input=false']) !== 0) {
    err('terraform init failed. Fix the issue above and run `node deploy.mjs` again to resume.');
    process.exit(1);
  }

  log();
  log(color('blue', '  → terraform apply'));
  if (await runTf(def.dir, ['apply', '-input=false', '-auto-approve']) !== 0) {
    err('terraform apply failed.');
    info([
      'If the error above mentions auth / invalid_grant / reauth (GCP) or expired',
      'credentials, refresh them and resume:',
      '  • GCP:          gcloud auth application-default login',
      '  • AWS:          aws sso login   (or refresh your access keys)',
      '  • DigitalOcean: generate a new API token and re-run',
      'Then run `node deploy.mjs` again and choose Resume — it picks up where it stopped.',
    ]);
    process.exit(1);
  }

  // ── Show outputs ──
  log();
  const out = spawnSync(TERRAFORM, [`-chdir=${def.dir}`, 'output', '-json'], { encoding: 'utf8' });
  let ip = '';
  let httpUrl = '';
  try {
    const o = JSON.parse(out.stdout || '{}');
    ip = o.public_ip?.value ?? '';
    httpUrl = o.url?.value ?? '';
    log(color('green', color('bold', '  🎉 Deployed!')));
    if (ip)      log(`     Public IP : ${color('bold', ip)}`);
    if (domain)  log(`     URL       : ${color('bold', `https://${domain}`)} ${color('dim', '(after DNS + cert)')}`);
    else if (httpUrl) log(`     URL       : ${color('bold', httpUrl)}`);
    if (o.ssh)   log(`     SSH       : ${color('dim', o.ssh.value)}`);
  } catch { /* ignore */ }

  log();
  log(color('bold', '  Next steps'));
  if (domain) {
    log(`    1. Point an A record for ${color('bold', domain)} at ${color('bold', ip || 'the IP above')}.`);
    log(`    2. Wait ~3–5 min for the first build, then Caddy auto-issues a TLS cert.`);
    log(`    3. Open ${color('bold', `https://${domain}`)} — the setup wizard creates your org + admin.`);
  } else {
    log(`    1. Wait ~3–5 min for the first build to finish on the VM.`);
    log(`    2. Open ${httpUrl || 'the URL above'} — the setup wizard creates your org + admin.`);
    log(color('dim', `    3. To add HTTPS later: point a domain at the IP, then set SITE_ADDRESS (and`));
    log(color('dim', `       WEB_URL/API_URL) in /opt/enlight/.env and run \`docker compose up -d\`.`));
  }
  log();
  log(color('dim', `  Tear down later with:  node deploy.mjs destroy`));
  log();
}

// ── Destroy flow ─────────────────────────────────────────────────────────────
async function destroy() {
  banner();
  if (!fs.existsSync(STATE_FILE)) { err('No previous deployment found (.enlight-deploy.json missing).'); rl.close(); return; }
  const { cloud, dir } = JSON.parse(fs.readFileSync(STATE_FILE, 'utf8'));
  warn(`This will DESTROY the ${CLOUDS[cloud]?.label ?? cloud} deployment and all its data.`);
  const sure = await confirm('Are you sure?', false);
  rl.close();
  if (!sure) { log('  Cancelled.'); return; }
  const tf = resolveTerraform();
  if (!tf) { err('Terraform not found (system PATH or ./bin). Install it, then retry.'); return; }
  TERRAFORM = tf;
  if (await runTf(dir, ['init', '-input=false']) !== 0) { err('terraform init failed.'); return; }
  if (await runTf(dir, ['destroy', '-input=false', '-auto-approve']) !== 0) {
    err('terraform destroy failed (see above) — resources may still exist. Re-auth if needed and retry.');
    return;
  }
  ok('Destroyed.');
}

// ── Entry ────────────────────────────────────────────────────────────────────
const cmd = process.argv[2];
const generateOnly = process.argv.includes('--generate-only');
try {
  if (cmd === 'destroy') {
    await destroy();
  } else if (cmd === 'install-terraform') {
    // Standalone: install Terraform without deploying anything.
    const existing = resolveTerraform();
    if (existing && existing !== localTfPath()) {
      ok(`Terraform is already installed (${existing}).`);
    } else {
      const v = await installTerraform();
      ok(`Terraform ${v} installed into ./bin/terraform.`);
    }
    rl.close();
  } else {
    await deploy(generateOnly);
  }
} catch (e) {
  err(e?.message ?? String(e));
  process.exit(1);
}
